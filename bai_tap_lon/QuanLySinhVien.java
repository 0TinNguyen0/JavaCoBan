        // TODO add your handling code here:
    JFileChooser chooser = new JFileChooser();
    int i = chooser.showSaveDialog(chooser);
    if (i == JFileChooser.APPROVE_OPTION) {
        File file = chooser.getSelectedFile();
    try {
            FileOutputStream f = new FileOutputStream(file + ".txt");
            ObjectOutputStream bwrite = new ObjectOutputStream(f);
            DefaultTableModel model = (DefaultTableModel) tbStudent.getModel();
    // ten Cot
    for (int j = 0; j < tbStudent.getColumnCount(); j++) {
        bwrite.writeObject(model.getColumnName(j) + "\t");
        }
        bwrite.writeObject("\n");
    // Lay du lieu dong
    for (int j = 0; j < tbStudent.getRowCount(); j++) {
    for (int k = 0; k < tbStudent.getColumnCount(); k++) {
        bwrite.writeObject(model.getValueAt(j, k) + "\t");
    }
        bwrite.writeObject("\n");
    }
        bwrite.close();
        JOptionPane.showMessageDialog(null, "Lưu file thành công!");
  } catch (Exception e2) {
        JOptionPane.showMessageDialog(null, "Lỗi khi lưu file!");
  }
    
    
    }//GEN-LAST:event_btnSaveActionPerformed
